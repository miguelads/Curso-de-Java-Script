/* 
  Serve para guarda um valor de uma comparação.
  Os únicos valores possiveis são:
   
     * True(Verdadeiro);
     * False(Falso);
     * 
     * ex:
     * console.log(5>2);
     * console.log(3>10);

*/
console.log(5>2);
console.log(3>10);

/*
  Maior que: >
  Menor que: <
  Maior ou igual: >=
  Menor ou igual: <=
  Igual: ==
  Diferente: !=
  Identico: === explicação identico é o mesmo valor e o mesmo tipo de variável.
*/

console.log(1 > 2);
console.log(5 < 10);
console.log(5 >= 3);
console.log(5 <= 4);
console.log(5 == 4);
console.log('Matheus' != 'Matheus');
console.log(3 ==='3');
console.log(4 === 4);